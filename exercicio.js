//Percorrer uma list de nomes
//Eduardo, Maria, Fermando, Joao e José 
//Numero da execução começando em 1
//Nome que está sendo executado 


const nome = ['Eduardo', 'Maria', 'Fernando', 'João', 'José']

nome.forEach((nomes, indice) => {
    console.log('Execução: ', indice)
    console.log('Nome: ' + nomes)
    console.log('--------------')
});



